import mongoose from 'mongoose';

// define user schema
const userSchema = new mongoose.Schema({
	name: {
		type: String,
		required: true,
	},
	email: {
		type: String,
		required: true,
		unique: true,
	},
	password: {
		type: String,
		required: true,
	},
});

// export model
export default mongoose.model('User', userSchema);
